view map_grasslandBase(uint16_t xIn, uint16_t yIn) {
	view me;
	memset(&me.screen,5,sizeof me.screen);
	memset(&me.layers,0,sizeof me.layers);
	uint32_t screenHash=0;
	screenHash |= xIn<<16;
	screenHash |= yIn;
	screenHash=lfsr(screenHash);
	int eX=(xIn-sX)*(SW*TS);
	int eY=(yIn-sY)*(SH*TS);
	printf("Wrapper: %d,%d\n",eX,eY);

	for(int x=0;x<SW;x++){
		for(int y=0;y<SH;y++) {
			if(screenHash & BIT(0) && screenHash & BIT(1)) me.screen[x][y]=6;
			if(screenHash & BIT(2) && screenHash & BIT(3) && screenHash & BIT(4) && screenHash & BIT(5)) {
				entitySpawn(ent_wall(30),eX+(TS*x),eY+(TS*y));
				me.layers[x][y]=1;
			}
			screenHash=lfsr(screenHash);
		}
	}
	if(screenHash & BIT(0)) {
		screenHash=lfsr(screenHash);
		me=offsetBlendMap(me,map_ridge(),screenHash%SH,0);
	}
	if(screenHash & BIT(0) && screenHash & BIT(1) && screenHash & BIT(3)) {
		screenHash=lfsr(screenHash);
		me=offsetBlendMap(me,map_ruinedhouse(),screenHash%3,0);
	}
	me.spawnFunc=NULL;
	return me;
}